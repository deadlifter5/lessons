//Введення даних
let dayNumber = parseInt(prompt("Ввести номер дня від 1 до 7:", "1"))

//Розрахуноk
if (dayNumber === 1) alert(`День тижня: Понеділок`)
else if (dayNumber === 2) alert(`День тижня: Вівторок`)
else if (dayNumber === 3) alert(`День тижня: Середа`)
else if (dayNumber === 4) alert(`День тижня: Четвер`)
else if (dayNumber === 5) alert(`День тижня: Пятниця`)
else if (dayNumber === 6) alert(`День тижня: Субота`)
else if (dayNumber === 7) alert(`День тижня: Неділя`)
else alert(`!Некорекне значення!`)
